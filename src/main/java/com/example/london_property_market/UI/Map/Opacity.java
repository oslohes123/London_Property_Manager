package com.example.london_property_market.UI.Map;

/**
 *
 * This enum serves as the available opacity options on the map. This enum provide more clarity and readability to the
 * options as opacity in base 16 (0xAA) would not. Moreover, the storage of the values in ints or in hashmaps in int may
 * obscure the meaning or the value of opacity.
 *
 * @author Yousef Altaher, K20047484
 * @version 03-18-2022
 */
public enum Opacity {
    DEFAULT_FILL_OPACITY,
    ZERO_OPACITY
}
